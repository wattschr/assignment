import io.grpc.Server;
import io.grpc.ServerBuilder;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;

import java.io.IOException;


@Component
public class BookJsonServer {
    private static final Logger logger = LoggerFactory.getLogger(BookJsonServer.class);

    private Server server;

    @Autowired
    private BookServerImpl bindableService;

    public void start() throws IOException {
    /* The port on which the data should run */
        int port = 50051;
        server = ServerBuilder.forPort(port)
                .addService(bindableService)
                .build()
                .start();
        logger.info("Server started, listening on " + port);
        Runtime.getRuntime().addShutdownHook(new Thread(() -> {
            // Use stderr here since the logger may have been reset by its JVM shutdown hook.
            System.err.println("*** shutting down gRPC data since JVM is shutting down");
            BookJsonServer.this.stop();
            System.err.println("*** data shut down");
        }));
    }

    private void stop() {
        if (server != null) {
            server.shutdown();
        }
    }

    /**
     * Await termination on the main thread since the grpc library uses daemon threads.
     */
    public void blockUntilShutdown() throws InterruptedException {
        if (server != null) {
            server.awaitTermination();
        }
    }

}
